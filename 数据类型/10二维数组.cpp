#include <iostream>
using namespace std;

/*
  二维数组定义：
    （1）数据类型   数组名[行数][列数];
    （2）数据类型   数组名[行数][列数] = {{值1，值2}，{值3，值4}};
    （3）数据类型   数组名[行数][列数] = {值1，值2，值3，值4};
    （1）数据类型   数组名[][列数] = {值1，值2，值3，值4};

*/

int main() {
  int numArr[2][3] = {{1, 2, 3}, {4, 5, 6}};
  // cout << numArr[0][0] << endl;
  // 外层循环打印行数，内层循环打印列数
  for (int i = 0; i < 2; i++) {
    for (int j = 0; j < 3; j++) {
      cout << numArr[i][j] << " ";
    }
    cout << endl;
  }
}